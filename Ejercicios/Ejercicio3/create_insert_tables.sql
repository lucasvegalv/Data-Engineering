-- Creacion de tablas e insercion de datos.

CREATE TABLE PROVEEDORES(
P# VARCHAR(2) PRIMARY KEY,
PNOMBRE VARCHAR(30),
CATEGORIA INT,
CIUDAD VARCHAR(30)
)

CREATE TABLE COMPONENTES(
C# VARCHAR(2) PRIMARY KEY,
CNOMBRE VARCHAR(30),
COLOR VARCHAR(15),
PESO INT,
CIUDAD VARCHAR(30)
)

CREATE TABLE ARTICULOS(
T# VARCHAR(2) PRIMARY KEY,
TNOMBRE VARCHAR(30),
CIUDAD VARCHAR(30)
)

CREATE TABLE ENVIOS(
P# VARCHAR(2),
C# VARCHAR(2),
T# VARCHAR(2),
CANTIDAD INT,

CONSTRAINT FK_P FOREIGN KEY (P#) REFERENCES PROVEEDORES(P#),
CONSTRAINT FK_C FOREIGN KEY (C#) REFERENCES COMPONENTES(C#),
CONSTRAINT FK_T FOREIGN KEY (T#) REFERENCES ARTICULOS(T#)
)

